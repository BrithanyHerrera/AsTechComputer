            <ul>
                <li><strong>Cookies propias:</strong> Son aquellas enviadas a su terminal directamente desde nuestro dominio gestionado por Astech Computer.</li>
                <li><strong>Cookies de terceros:</strong> Son enviadas desde equipos o dominios gestionados por entidades externas que nos prestan servicios, tales como proveedores, redes sociales o integraciones funcionales, para tratar los datos que obtienen a través de estas.</li>
            </ul>

            <p><strong>B. Según el plazo de tiempo que permanecen activadas:</strong></p>
            <ul>

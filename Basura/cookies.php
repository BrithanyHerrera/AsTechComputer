            <ul>
                <li><strong>Cookies técnicas:</strong> Permiten la navegación a través de la plataforma y el uso de servicios esenciales como el control de tráfico, identificación de sesión, acceso a partes restringidas o uso de elementos de seguridad durante la navegación.</li>
                <li><strong>Cookies de personalización:</strong> Permiten que el sitio web recuerde opciones predefinidas por el usuario, como el tipo de navegador, la configuración regional o las preferencias seleccionadas.</li>
                <li><strong>Cookies analíticas:</strong> Nos permiten cuantificar el número de usuarios y realizar análisis estadísticos sobre la utilización de nuestra web para mejorar continuamente la oferta de nuestros servicios tecnológicos.</li>
            </ul>

